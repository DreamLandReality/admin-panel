export const EDITOR_ICON_CATEGORIES = Array.from(new Set(EDITOR_ICON_CATALOG.map((icon) => icon.category)))

export function searchEditorIcons(query: string, category?: string): EditorIconEntry[] {
  let results = EDITOR_ICON_CATALOG
  if (category && category !== 'All') {
    results = results.filter((icon) => icon.category === category)
  }
  if (query.trim()) {
    const normalizedQuery = query.toLowerCase()
    results = results.filter((icon) =>
      icon.name.includes(normalizedQuery) ||
      icon.label.toLowerCase().includes(normalizedQuery) ||
      icon.category.toLowerCase().includes(normalizedQuery)
    )
  }
  return results
}

export function getEditorIconByName(name: string): EditorIconEntry | undefined {
  return EDITOR_ICON_CATALOG.find((icon) => icon.name === name)
}
